import type { StatsGroupBy, StatsMode } from '@/shared/stats/types';

/** Короткие подсказки под заголовками блоков (UX). */
export function primaryChartHint(mode: StatsMode, groupBy: StatsGroupBy): string {
  switch (mode) {
    case 'correlation':
      return 'Динамика факторов по периодам и целевой метрики «Успех, %». Для читаемости оставьте 2-3 серии через фильтр.';
    case 'rank':
      return 'По горизонтали — выбранная агрегация дат; по вертикали — очки за день. Наведите на точку для подсказки.';
    case 'finance':
      if (groupBy === 'categories') {
        return 'Линия — сальдо по периодам (сумма выбранных «Доходы» + «Расходы»). Второй график показывает разложение на потоки.';
      }
      return 'Столбцы — суммы по статьям за период. Второй график — сводка доход/расход/баланс по тем же датам.';
    case 'time':
    case 'leisure':
      return 'Высота столбца — часы за период. Наведите — точные значения по каждой серии.';
    case 'nutrition':
      if (groupBy === 'elements') {
        return 'Столбцы — калории по продуктам и пресетам (блюда как готовые записи не суммируются — учитываются продукты). Второй график — топ по ккал.';
      }
      return 'Столбцы — БЖУ и калории по периодам (без «блюд» как отдельных строк — это наборы продуктов). Второй график — доли за период.';
    case 'mood':
      return 'Линия — уровень настроения по дням с записью. Пустые дни не соединяются нулём в таблице.';
    case 'tasks':
    case 'rituals':
      return 'Линии — выполнение в %. Наведите на точку — все категории или задачи за этот интервал.';
    default:
      return 'Наведите на график — всплывающая подсказка покажет все серии за выбранный интервал.';
  }
}

export function secondaryChartHint(mode: StatsMode, groupBy: StatsGroupBy): string {
  if (mode === 'rank') {
    return 'Накопительные очки ранга на конец каждого периода агрегации. Наведите на точку — значение.';
  }
  if (mode === 'correlation') {
    return 'Коэффициент корреляции Пирсона между каждым фактором и «Успех, %». Ближе к +1 — сильная прямая связь, к -1 — обратная.';
  }
  if (mode === 'finance') {
    return 'Доходы и расходы по каждому периоду + линия баланса. Так проще увидеть и оборот, и итог.';
  }
  if (mode === 'tasks' || mode === 'rituals') {
    return 'Рейтинг серий по среднему выполнению за выбранный диапазон. Сразу видно лидеров и просадки.';
  }
  if (mode === 'time' || mode === 'leisure') {
    return 'Топ серий по сумме часов за весь диапазон. Фильтр серий и группировка «категории/элементы» влияют на набор.';
  }
  if (mode === 'nutrition' && groupBy === 'elements') {
    return 'Топ элементов по суммарным калориям за диапазон. Удобно сравнивать продукты и пресеты.';
  }
  if (mode === 'nutrition') {
    return 'Сводка долей: суммарные Белки, Жиры, Углеводы и Калории за весь выбранный период (на основной график — динамика по датам).';
  }
  if (mode === 'mood') {
    return 'Круг — сколько дней с каждым уровнем настроения за весь диапазон. Справа — доли и числа.';
  }
  return 'Сводка за весь выбранный диапазон дат: сумма значений по каждой серии. Наведите на сегмент — детали.';
}

